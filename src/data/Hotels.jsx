export const getHotelById = (id) => {
  return hotels.find(hotel => hotel.id === id);
};

export const getFeaturedHotels = () => {
  return hotels.filter(hotel => hotel.featured);
};

export const searchHotels = (filters) => {
  return hotels.filter(hotel => {
    if (filters.destination) {
      const searchTerm = filters.destination.toLowerCase();
      const matchesCity = hotel.city.toLowerCase().includes(searchTerm);
      const matchesCountry = hotel.country.toLowerCase().includes(searchTerm);
      const matchesName = hotel.name.toLowerCase().includes(searchTerm);
      if (!matchesCity && !matchesCountry && !matchesName) return false;
    }

    if (filters.priceRange) {
      if (hotel.pricePerNight < filters.priceRange[0] || hotel.pricePerNight > filters.priceRange[1]) {
        return false;
      }
    }

    if (filters.starRating && filters.starRating.length > 0) {
      if (!filters.starRating.includes(hotel.stars)) return false;
    }

    if (filters.amenities && filters.amenities.length > 0) {
      const hasAllAmenities = filters.amenities.every(amenity =>
        hotel.amenities.includes(amenity)
      );
      if (!hasAllAmenities) return false;
    }

    return true;
  });
};
